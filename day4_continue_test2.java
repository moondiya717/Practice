package April30.Practice;

public class day4_continue_test2 {

	public static void main(String[] args) {
		/*1부터 10까지의 짝수들을 continue를 이용하여 출력하는 코드를 작성하세요.*/ 
		//for문 실행에다가 출력하기 입력해야 함. for끝나고 sysout하면 값이 잘못 나옴.
		int i;
		for(i=1; i<=10; i+=1) {
			if(i % 2 != 0) {
				continue;
			} System.out.print(i + " ");
		} 
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
